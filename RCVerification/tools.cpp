////////////////////  Define BRDF Function   //////////////////////////////////////////

inline REAL Compute_fr(Vector w1, Vector w2){
	Vector wr = Vector(-w1.x, -w1.y, w1.z);
	return pow( Dot(wr,w2), default_roughness) * (default_roughness_f + 1.0) / (2.0 * PI);
}

